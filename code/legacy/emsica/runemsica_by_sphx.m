%% ==== Appendix ======
% 
% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% % save EEG for futhur plotting
% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% %  Note:
% %    The activation source, s, 
% %    EEG.icaact = (EEG.icaweights*EEG.icasphere)*EEG.data;
% %
% %    s should be the same as what it really working in EMSICA
% %                 s = w * Xtilde
% %    the activation source:
% %                 s = Wx 
