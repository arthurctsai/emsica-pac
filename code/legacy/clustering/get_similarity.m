                    disp(['cannot find ' mypath(filename)]);
                    keyboard
                end
                comps = sk(sk(:,1)==s,2);
                allbeta(end+1:end+length(comps),:)=fS(comps,:);
            end
            betacorr = zeros(size(allbeta,1));
            for j=1:size(allbeta,1)
                for k = 1:size(allbeta,1)
                    r=corrcoef(allbeta(j,:),allbeta(k,:));
                    r=abs(r);
                    betacorr(j,k) = r(1,2);
                end
            end
            figure;imagesc(betacorr);title('Beta corr');
    end % switch features{i}

end
