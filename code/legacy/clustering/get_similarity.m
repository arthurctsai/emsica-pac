for i = 1:length(features)
    switch lower(features{i})
        case 'topomi'
            similarity = weightings(i)*topomi+similarity;
        case 'topoemd'
            similarity = weightings(i)*topoemd+similarity;
        case 'topo67x67'
            similarity = weightings(i)*topocorr+similarity;
        case 'spec'
            similarity = weightings(i)*speccorr+similarity;
        case 'topo11'
            similarity = weightings(i)*tp11corr+similarity;
        case 'beta'
            similarity = weightings(i)*betacorr+similarity;
        case {'b','bmat'}
            similarity = weightings(i)*allBcorr+similarity;
    end
    if any(any(isnan(similarity)))
        mdisp(features{i})
        keyboard;
    end
end
